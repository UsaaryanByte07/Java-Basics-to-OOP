public class ArithematicOperator_07 {
    public static void main(String[] args) {
        int a = 8;
        int b = 5;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b); //integer divided by integer is a integer
        System.out.println((float)a/(float)b);
        System.out.println(a%b);

    }
}
